/*
max n compare
0 → strings are equal
< 0 → str1 is less than str2
> 0 → str1 is greater than str2
compare ascii values -> s1 - s2*/
int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n == 0)
		return (0);
	while (i < n && s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}
